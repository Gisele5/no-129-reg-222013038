public class break3 {
    public static void main(String[] args) {    
        int i=1;  
        //do-while loop  
        do{  
            if(i==5){  //condition
               i++;  
               break; //switch statement in which it is present
            }  
            System.out.println(i);  
            i++;  //increment
        }while(i<=10);  
    }  
    }     

